                       <H4>
                        Traitement
                       </H4> <br>

                       </div> 
                       <div class="parag3">
                        Il n’existe présentement aucun traitement curatif pour la MC; toutefois, elle peut être bien traitée à l’aide d’un régime alimentaire sans gluten. Le respect du régime alimentaire sans gluten doit être STRICT et À VIE. Un régime alimentaire sans gluten peut être exigeant et lourd; ainsi, il est primordial de consulter un diététiste professionnel ayant une expertise dans le régime alimentaire sans gluten.<BR>

                        Les individus atteints de la MC doivent être vigilants face aux sources cachées de gluten, ainsi qu’à la contamination croisée des produits alimentaires. Il est essentiel de procéder à la lecture minutieuse des étiquettes à chaque achat de produit.
                        
                        Puisque la maladie cœliaque est un problème médical chronique, un suivi régulier à long terme par un médecin et un diététiste est recommandé.
                        
