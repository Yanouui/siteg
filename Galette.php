    <div class="TEXT1">
        <h3>Galettes à la pistache sans gluten</h3> <br>


        <div class="PARAG">

            <P>Clin d’œil à la Saint Patrick, ces galettes sans gluten à la pistache se picorent avec gourmandise, avec
                une boisson chaude.<br> Idéales pour tea time! Bien que le glaçage au blanc d’oeuf apporte une jolie
                finition… on ne va pas faire que les regarder ces belles galettes!
            </P>


            <div class="INGRÉDIENTS">
                <h3>INGRÉDIENTS </h3>
                <ul>
                    <li> &nbsp; 100 g de farine riz Sans Gluten.</li>
                    <LI> &nbsp; 100 g de pistaches non sucrées réduites en poudre.</li>
                    <li> &nbsp; 90 g de sucre. </li>
                    <li> &nbsp; 80 g de purée d’amande blanche.</li>
                    <li> &nbsp; 2 œufs (1 pour les galettes + 1 jaune pour la dorure + 1 blanc pour le glaçage). </li>
                    <li> &nbsp; 1 pincée de sel. </li>
                    <li> &nbsp; 125 g de sucre glace.</li>
                    <li> &nbsp; 1 cuillère à soupe de jus de citron ou d’eau chaude.</li>
                    <li> &nbsp; Colorant alimentaire (pour une couleur verte : 5 gouttes de jaune et 3 gouttes de bleu).</li>
                </ul>
            </div>

        </div>
    </div>


    <div class="REALI">

       <h3>RÉALISATION DE LA PÂTE SABLÉE</h3>
        <br>


        <P>
            Mélanger le sucre, la farine, la poudre de pistache, la pincée de sel et la purée d’amande. Mélanger du bout
            des doigts pour confectionner un sable grossier.

            Ajouter un œuf entier, légèrement battu en omelette. Mélanger jusqu’à obtenir une pâte homogène.

            Réserver la pâte de pâte 1h30 au réfrigérateur ou 30 minutes au congélateur pour les plus pressé(e)s.
        </P>
        <br>

        <h3>RÉALISATION DES GALETTES</h3>
        <br>
        <P>
            Étaler la pâte sur un papier cuisson très légèrement fariné, en veillant à placer un film étirable entre
            celle-ci et le rouleau.<br> L’épaisseur doit s’approcher de 5 mm. A l’aide d’emporte-pièces, découper des
            formes et les disposer sur une plaque de cuisson recouverte de papier. <BR>Former une boule avec les chutes
            de pâte et l’abaisser à nouveau jusqu’à épuisement (une vingtaine de galettes).<br>

            Dorer les biscuits avec le jaune d’oeuf délayé dans une cuillère à soupe d’eau.<br>

            Enfourner pour 10 à 15 minutes à 180°C en surveillant bien la coloration.<br>

            Laisser les galettes refroidir avant de les décorer.


        </P>
        <br>



        <h3>RÉALISATION DU GLAÇAGE</h3> <br>
        <p>
            Pour le glaçage, mélanger à la fourchette le blanc d’oeuf avec le citron et le sucre glace.<br> Agrémenter
            éventuellement de colorant alimentaire. Déposer à la petite cuillère une dose raisonnée de glaçage sur
            chaque galette et l’étaler délicatement à l’aide d’un cure-dent.<br>

            Laisser figer à température ambiante.<br>

            Les galettes se conservent une semaine dans une boîte métallique.<BR>
        <H1 style="color: #000;"> <br>
            Bon appétit !
        </H1>
        </p>

