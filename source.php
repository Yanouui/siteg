    <li> <h1> Boissons distillées et vinaigres</h1> </li>
    
  <p class="para">

La plupart des boissons distillées et des vinaigres sont sans gluten. Ces produits distillés ne contiennent aucun
peptide de gluten nocif, même s'ils sont fabriqués à partir de céréales contenant du gluten. La recherche indique que le
peptide de gluten est trop gros pour être transféré dans le processus de distillation, laissant le liquide résultant
sans gluten. Cependant, certains types de boissons alcoolisées contiennent une quantité dangereuse de gluten pour les
personnes atteintes de la maladie cœliaque, et comprennent celles avec une couleur ou un arôme ajouté comme les vins de
dessert et celles à base de malt d'orge, comme les refroidisseurs de vin en bouteille. Pour ceux-ci, les consommateurs
doivent vérifier l'étiquette et, en cas de doute, contacter l'entreprise. , ales, lagers, boissons maltées et vinaigres
de malt fabriqués à partir de grains contenant du gluten ne sont pas distillés et ne sont donc pas sans gluten.

