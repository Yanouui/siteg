        <div class="TITRE">
            <h1>
                Dermatite herpétiforme:
            </H1>
        </div> <br>
        <div class="soustitre">
            <h4>
                Définition:
            </h4>
        </div> <br>
        <div class="parag">
            La dermatite herpétiforme (DH) est la « maladie cœliaque de la peau ». Il s’agit d’un problème cutané
            présentant une éruption cutanée caractéristique accompagnée de démangeaisons et de sensations de brûlure
            intenses.
        </div> <br>
        <div class="soustitre">
            <H4>
                Cause:
            </H4>
        </div> <br>
        <div class="parag">
            Des facteurs génétiques, le système immunitaire et la sensibilité au gluten jouent un rôle dans ce problème
            médical. Les mécanismes précis demeurent inconnus
        </div> <br>
        <div class="soustitre">
            <H4>
                Incidence:
            </H4>
        </div> <br>
        <div class="parag">
            La prévalence de la DH est d’environ 10 cas sur 100 000 personnes. Elle est plus commune chez les hommes.
            L’apparition est plus fréquente entre la vingtaine et la quarantaine. Elle est rare chez les enfants. Entre
            10 % et 15 % des patients atteints de la maladie cœliaque sont également atteints de la DH.
        </div> <br>
        <div class="soustitre">
            <H4>
                Caractéristiques cliniques:
            </H4>
        </div> <br>
        <div class="parag">
            Une nouvelle lésion sans éraflure est rouge, boursouflée et généralement petite, et possède une petite
            cloque en son centre. Toutefois, si vous l’éraflez, des croûtes apparaîtront à la surface. La sensation de
            brûlure ou de picotement peut être très intense. Les zones les plus communes sont les coudes, les genoux, la
            nuque, le cuir chevelu, le haut du dos et les fesses. Des lésions faciales et à la racine des cheveux ne
            sont pas inhabituelles. L’intérieur de la bouche est rarement atteint. L’éruption cutanée est symétrique.
        </div> <br>
        <div class="soustitre">
            <H4>
                Diagnostic:
            </H4>
        </div> <br>
        <div class="parag">
            La DH peut être diagnostiquée par une biopsie de la peau saine adjacente aux cloques ou des érosions. La
            grande majorité des patients présentera également des caractéristiques de la maladie cœliaque (atrophie des
            villosités) et une biopsie duodénale ne sera pas nécessaire à la confirmation du diagnostic. Les symptômes
            cutanés prédominent généralement les symptômes intestinaux. Les résultats aux tests sérologiques de la
            maladie cœliaque pourraient être négatifs. Comme c’est le cas de la maladie cœliaque, les patients
            pourraient présenter des carences nutritionnelles. Des tests en laboratoire devraient être effectués; y
            compris un hémogramme complet, des tests sur le fer, l’albumine, la phosphatase alcaline, le folate, le
            phosphate, le calcium et la vitamine D, comme appropriés pour chaque patient
        </div> <br>
        <div class="soustitre">
            <h4>
                Prise en charge:
            </h4>
        </div> <br>
        <div class="parag">
            Le traitement de la DH consiste en la prise de médicaments et en un régime alimentaire sans gluten. Le
            dapsone oral (Avlosulfon) est souvent utilisé pour traiter l’éruption cutanée. La réponse est souvent
            dramatique avec un soulagement rapide de la brûlure et une amélioration de l’éruption cutanée.

            Un régime alimentaire sans gluten strict devrait être entamé; celui-ci devrait éliminer les aliments et les
            boissons contenant du blé, du seigle ou de l’orge. Il est préférable de recommander le patient à un
            diététiste ayant une expertise du régime alimentaire sans gluten.

            Puisque la DH est un problème médical chronique, il est important que le patient qui en est atteint soit
            régulièrement suivi par un médecin et un diététiste.
        </div> <br>
