    <div class="pate">
        <h3>RÉALISER DES PÂTES À CRÊPES SANS GLUTEN</h3> <br>
        <P> Peser dans une jatte les farines. Ajouter les oeufs entiers et progressivement le lait en fouettant vivement
            jusqu’à former une pâte à crêpes homogène. Prélever un tiers de la pâte pour la placer dans un autre
            récipient. Y incorporer le cacao et mélanger
        </P> <br>

        <h3>CUIRE LES CRÊPES EN FORMANT DES TOURBILLONS</h3><br>
        <P>Dans une poêle crêpière bien chaude, légèrement huilée, dessiner un tourbillon avec la pâte au cacao à l’aide
            d’un saladier muni d’un bec verseur ou tout simplement d’une grande cuillère.<BR> Laisser cuire quelques
            secondes puis verser une louche de pâte nature pour qu’elle recouvre l’ensemble (si les pâtes sont trop «
            lourdes », les délayer avec un peu d’eau). Laisser cuire la crêpe quelques instants de chaque côté puis
            passer à la suivante et ainsi de suite jusqu’à épuisement des ingrédients.<BR>

            Garnir les crêpes à votre convenance. <br> <br>
