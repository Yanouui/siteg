                <li> &nbsp; 100 g de farine de riz demi-complète</li>
                <LI> &nbsp; 100 g de farine de sarrasin </li>
                <li> &nbsp; 3 oeufs bio</li>
                <li> &nbsp; 50 cl de lait végétal au choix </li>
                <li> &nbsp; 2 cuillères à soupe de cacao en poudre (non sucré)</li>
                <li>&nbsp; Huile pour la cuisson</li>

            </ul>

