            <P>Pour fêter la Chandeleur de façon conviviale et originale, succombez aux crêpes marbrées au cacao sans
                gluten ! Faites parler votre fibre artistique pour insérer de beaux tourbillons de cacao au sein de vos
                crêpes. Chacune sera unique ! Cette recette sans sucre ajouté vous permet ensuite de choisir la
                garniture de votre choix : miel, chocolat, rondelles de banane ou crème de coco fouettée en mode
                chantilly…

            </P>
