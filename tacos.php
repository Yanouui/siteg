        <h3> Matériel:</h3> <br>
        <UL>
            <LI> Film alimentaire</LI>
            <LI> Poêle</LI>
            <LI> Saladier</LI>
        </UL>
        <br>

        <h3>Préparation:</h3> 


        <UL>
         &nbsp;   <h4>Pour les tortilas</h4> <br>
            <LI>
                Dans un saladier, mélanger farine, fécule et sel. Faire un puits au milieu puis casser les oeufs.
                Mélanger en raclant peu à peu la farine sur les bords jusqu'à ce que la pâte soit épaisse puis commencer
                à rajouter petit à petit l'eau. Laisser le tout reposer 1/2 heure 3/4 d'heure avec un film alimentaire
                sur le saladier.< Après ce temps de repos rajouter le lait de riz. On obtient une pâte type pâte à
                    gâteau en un peu plus liquide. Prendre une poêlé, un peu de matière grasse végétale (huile d'olive
                    ici), et une louche et c'est parti !</LI> <br>

            <LI>
                Tout d'abord, faire revenir l'ail dans de l'huile d'olive puis le poulet émincé à feu vif. Saler,
                poivrer, et ajouter le piment. Réserver le poulet dans une assiette creuse une fois saisit afin de
                recueillir le jus. Puis faire roussir les oignons avec une pincée d'herbe de Provence et rajouter le jus
                du poulet. Ajouter ensuite les poivrons et les tomates coupés respectivement en lanières et en cubes
                grossiers, mélanger et couvrir. Laisser cuire à feu moyen pendant une vingtaine de minutes en remuant de
                temps en temps pour ne pas que la garniture accroche. Rajouter le poulet en fin de cuisson puis
                ré-assaisonner si besoin. <br>


            </LI>
            <LI>
                Servir avec de la crème fraîche et un guacamole fait maison (un avocat écraser, 1/4 de tomate détaillée
                en petit dés et assaisonner au piment, sel et poivre avec un filet de citron).
                Un plat conviviale et sans gluten qui ravira tous les gourmands, même ceux qui mange normalement !
            </LI>



        </UL>

