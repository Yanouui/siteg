    <div class="TEXT">

        <h3>Sablés sans gluten, une recette toute simple</h3> <br>

        <div class="PARA">
            <P>
                Voilà une recette rapide et facile de sablés sans gluten, ils sont nature mais vous pouvez les parfumer
                en ajoutant des zestes de citron, de la noix de coco ou une cuillère de thé matcha à la recette de
                base.<BR>
                Ces petits sablés sont bien croquants, très bon pour le gouter avec un thé ou un café.<BR> On peut aussi
                les utiliser en recette par exemple je m’en sers souvent pour faire la base de mes cheesecakes sans
                gluten.
            </P>
        </div> <br>
        <div class="INGRÉDIENTS">
            <h3>INGRÉDIENTS </h3> <br>
            <UL>
                <li> 200g de farine Sans Gluten </li>
                <LI> ½ cuillère à café de poudre à lever Ma Vie Sans Gluten</li>
                <li>
                    80 g de margarine sans huile de palme à température ambiante ou d’huile de coco</li>
                <li> 60 g de sucre blond de canne </li>
                <li> 50 ml d’eau </li>

                <li>150 g de chocolat noir 70% de cacao</li>
                <LI> 60 g de crème végétale liquide</LI>
            </UL>
        </div>
    </div>
    <div class="P2">
        <h3>GANACHE</h3> <br>


        <P>
            Faire fondre au bain-marie le chocolat avec la crème végétale. Laisser refroidir puis stocker au frais afin
            que la ganache durcisse légèrement.
        </P>
<br>
        <h3>PÂTE À BISCUITS SANS GLUTEN</h3> <br>
        <P>
            Dans une petite casserole, peser le beurre et le sucre. Ajouter 50 ml d’eau et chauffer doucement. Couper à
            l’ébullition et laisser refroidir quelques instants.<BR> Placer la farine et la poudre à lever dans un
            saladier.<BR> Verser le mélange précédent et pétrir l’ensemble pour obtenir une pâte bien homogène.<BR> La
            filmer puis réserver au frais pour 1 à 2 heures.<BR>

            Déposer la pâte sur un papier cuisson. L’étaler délicatement au travers d’un film étirable pour obtenir une
            épaisseur homogène de 3 mm environ.<BR> A l’aide d’emporte-pièce, tailler un nombre paire de biscuits.<BR>
            En décorer éventuellement la moitié en découpant l’intérieur à l’aide d’emporte-pièces plus petits ou bien
            en utilisant la pointe d’un couteau et une paille pour dessiner des visages.<BR> Disposer les biscuits sur
            une plaque recouverte de papier cuisson. Enfourner pour 11 minutes à 180°C en surveillant pour éviter une
            trop forte coloration. Laisser refroidir sur une grille
        </P>
    </div> <br>
    
    <div class="P3">
        <h3>MONTAGE</h3> <br>
        <p>
            Garnir les biscuits sans décors de ganache au moyen d’une cuillère ou d’une poche à douille.<BR> Fermer les
            choco façon BN© sans gluten avec un second biscuit non décoré et conserver dans une boîte métallique.<BR>
        <H1 style="color:  #1a1515;">
            Bon appétit !
        </H1>
        </p>
