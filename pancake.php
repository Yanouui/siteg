        <h3>Pancake sans gluten</h3> <br>
        <div class="PARAG">
            <P>
                Pour le petit déjeuner, le goûter ou le dessert, on se régale avec les pancakes sans gluten !<BR>
                Sans gluten et totalement vegan, cette recette met la patate douce à l’honneur.<BR>
                Une belle association avec le sirop d’érable et les fruits rouges…
            </P> <br>
            <div class="INGRÉDIENTS">
                <h4>INGRÉDIENTS: </h4> <br>
                <ul>


                    <li> 100 g de purée de patate douce bio </li>
                    <LI>225 ml de lait végétal au choix (= 225 g)</li>

                    <li>
                        20 g de sirop d’érable
                    </li>
                    <li>
                        75 g de farine de riz

                    </li>
                    <li>
                        50 g de fécule

                    <li>
                        1 cuillère à soupe de poudre à lever
                    </li>

                </ul>

            </div>

        </div>


      
    

    </div>
    <div class="CONF">



        <h3>CONFECTION DE LA PÂTE À PANCAKES SANS GLUTEN</h3> <br>


        <P>
            Mélanger la purée de patate douce avec le sirop d’érable et le lait végétal.<BR>

            Incorporer la farine, la fécule et la poudre à lever en mélangeant vivement.
        </P> <br>

        <h3>CUISSON DES PANCAKES SANS GLUTEN</h3> <br>
        <P>
            Faire chauffer à feu vif une poêle crêpière huilée.<br>

            Avec une louche ou un saladier à bec verseur, déposer des petits tas de pâte.<BR> Les faire cuire 1 minute
            de chaque côté et les réserver dans une assiette.<br> La quantité de pâte permet de réaliser une quinzaine
            de pancakes sans gluten de taille moyenne.<br>

            Servir les pancakes avec du sirop d’érable et des fruits rouges ou bien du chocolat fondu.<br>

            Bonne dégustation !

            Déposer la pâte sur un papier cuisson.<BR> L’étaler délicatement au travers d’un film étirable pour obtenir
            une épaisseur homogène de 3 mm environ.<BR> A l’aide d’emporte-pièce, tailler un nombre paire de
            biscuits.<BR> En décorer éventuellement la moitié en découpant l’intérieur à l’aide d’emporte-pièces plus
            petits ou bien en utilisant la pointe d’un couteau et une paille pour dessiner des visages. Disposer les
            biscuits sur une plaque recouverte de papier cuisson. <BR>Enfourner pour 11 minutes à 180°C en surveillant
            pour éviter une trop forte coloration.<BR> Laisser refroidir sur une grille<BR>
        <H1 style="color:  #1730c1;"> <br>
            Bon appétit !
        </H1>
        </P>
