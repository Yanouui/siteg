        <div class="TITRE">
            <h1>
                Régime sans gluten: 4 erreurs à ne pas faire
            </H1>
        </div> <br>
        <div class="soustitre">
            <h4>
            ● Ne pas faire confiance aux produits estampillés «sans gluten»
            </h4>
        </div> <br>
        <div class="parag">
            L’industrie agroalimentaire a flairé le filon et l’on trouve aujourd’hui des glaces, du jambon, des laitages et bien
            d’autres choses sans gluten. Le problème de ces préparations, c’est qu’elles n’échappent pas aux inconvénients des plats
            préparés avec ajouts de sucre, de sel, etc. D’ailleurs des études ont prouvé que, dans certains cas, ces régimes à base
            d’aliments modifiés pouvaient faire grossir! La règle: aller aux produits et céréales naturellement sans gluten.
        </div> <br>
        <div class="soustitre">
            <H4>
            ● Ne pas remplacer le pain et les pâtes par un seul produit
            </H4>
        </div> <br>
        <div class="parag">
            Les intolérances sont souvent causées par la consommation excessive de certains produits. Renoncer au blé et ne
            consommer que du riz, du sarrasin ou des pommes de terre peut s’avérer contre-productif et déclencher d’autres formes
            d’intolérance. L’une des règles essentielles que rappellent les nutritionnistes reste le besoin de «rotation
            alimentaire». Se nourrir d’un peu de tout et ne surtout pas se restreindre à manger une seule chose en quantité sur une
            période donnée.
        </div> <br>
        <div class="soustitre">
            <H4>
            ● Faire son pain n’est pas très utile
            </H4>
        </div> <br>
        <div class="parag">
            Partir du principe que le pain de son boulanger est toxique et vouloir le faire soi-même peut paraître relativement
            logique dans une démarche «no glu». Mais l’idée présente deux inconvénients. D’abord, faire du bon pain reste un métier.
            Ne s’improvise pas boulanger qui veut. Ensuite, beaucoup de médecins rappellent que l’on met parfois sur le dos du
            gluten des intolérances dues aux additifs et autres compléments nécessaires pour réaliser un bon pain. <br>
            Le conseil d’utiliser du petit épeautre est aussi sujet à caution car il contient du gluten, contrairement à ce que l’on
            peut parfois entendre. Certes en moindre quantité mais, en cas d’intolérance, le changement risque d’être inefficace. Au
            final, la meilleure solution est de se fournir chez un vrai boulanger spécialisé dans le «sans gluten».
        </div> <br>
        <div class="soustitre">
            <H4>
            ● Attention à la sortie de régime
            </H4>
        </div> <br>
        <div class="parag">
            Comme tous les régimes, le «no glu» est contraignant et n’échappe pas à la tentation de le stopper au bout de trois ou
            six mois. Si c’est le cas, il faut réintroduire le pain, les pâtes ou les pizzas progressivement. C’est un bon moyen de
            voir quand, comment et pourquoi l’intolérance se manifeste. Et dites-vous qu’une bonne sortie de régime, même un peu
            lente, laisse miroiter le voyage en Italie sans restriction au bout de la fourchette. Plutôt motivant, non?
        </div> <br>
        
        
